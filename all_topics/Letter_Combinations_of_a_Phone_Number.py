class Solution:
    def letterCombinations(self, digits: str) -> List[str]:
        # low performance
        num_dict = {'2':['a','b','c'],
                 '3':['d','e','f'],
                 '4':['g','h','i'],
                 '5':['j','k','l'],
                 '6':['m','n','o'],
                 '7':['p','q','r','s'],
                 '8':['t','u','v'],
                 '9':['w','x','y','z']}

        res_ls = []
        len_digits = len(digits)

        if len_digits == 0:
            return res_ls

        first = num_dict[digits[0]]
        if len_digits == 1:
            return first
        else:
            for i in first:
                for j in self.letterCombinations(digits[1:]):
                    res_ls.append(i+j)

            return res_ls

    def letterCombinations(self, digits: str) -> List[str]:
        if not digits:
            return list()

        phoneMap = {
            "2": "abc",
            "3": "def",
            "4": "ghi",
            "5": "jkl",
            "6": "mno",
            "7": "pqrs",
            "8": "tuv",
            "9": "wxyz",
        }

        def backtrack(index: int):
            if index == len(digits):
                combinations.append("".join(combination))
            else:
                digit = digits[index]
                for letter in phoneMap[digit]:
                    combination.append(letter)
                    backtrack(index + 1)
                    combination.pop()

        combination = list()
        combinations = list()
        backtrack(0)
        return combinations

